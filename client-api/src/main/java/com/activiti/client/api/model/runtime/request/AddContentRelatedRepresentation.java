package com.activiti.client.api.model.runtime.request;

import com.activiti.client.api.model.common.AbstractRepresentation;

public class AddContentRelatedRepresentation extends AbstractRepresentation
{
    protected String name;

    protected boolean link;

    protected String source;

    protected String sourceId;

    protected String mimeType;

    public AddContentRelatedRepresentation(String name, boolean link, String source, String sourceId, String mimeType)
    {
        setName(name);
        setLink(link);
        setSource(source);
        setSourceId(sourceId);
        setMimeType(mimeType);
    }

    public String getMimeType()
    {
        return mimeType;
    }

    public void setMimeType(String mimeType)
    {
        this.mimeType = mimeType;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public boolean isLink()
    {
        return link;
    }

    public void setLink(boolean link)
    {
        this.link = link;
    }

    public String getSource()
    {
        return source;
    }

    public void setSource(String source)
    {
        this.source = source;
    }

    public String getSourceId()
    {
        return sourceId;
    }

    public void setSourceId(String sourceId)
    {
        this.sourceId = sourceId;
    }

}
